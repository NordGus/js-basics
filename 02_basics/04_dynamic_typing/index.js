// Dynamic Typing

let name = 'Mosh';
console.log(typeof(name)); // returns "string"
name = 30.1;
console.log(typeof(name)); // returns "number"
name = true;
console.log(typeof(name)); // returns "boolean"
name = undefined;
console.log(typeof(name)); // returns "undefined"
name = null;
console.log(typeof(name));  // returns "object"?