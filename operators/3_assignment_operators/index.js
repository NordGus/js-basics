// Assingment Operators

let x = 10; // Simple assigment operator
console.log(x);

x += 5; // Addition assigment operator. Equivalent to x = x + 5
console.log(x);

x = 10

x -= 5; // Substraction assigment operator. Equivalent to x = x - 5
console.log(x);

x = 10

x *= 5; // Multiplication assigment operator. Equivalent to x = x * 5
console.log(x);

x = 10

x /= 5; // Division assigment operator. Equivalent to x = x / 5
console.log(x);

x = 10

x %= 5; // Division rest assigment operator. Equivalent to x = x % 5
console.log(x);

x = 10

x **= 5; // Exponent assigment operator. Equivalent to x = x ** 5
console.log(x);