// Arithmetic Operators

let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y); // exponetiation, x top the power of y

// Increment (++)
console.log(x);
console.log(++x); // First increments by 1 the value of x then excute the rest of the code
console.log(x++); // First excute the rest of the code then increments by 1 the value of x
console.log(x);

// Decrement (--)
console.log(x);
console.log(--x); // First decrements by 1 the value of x then excute the rest of the code
console.log(x--); // First excute the rest of the code then decrements by 1 the value of x
console.log(x);