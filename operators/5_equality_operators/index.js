// Equality Operators


console.log('Strict Equality Operator (===). Type and Value must be equal', 1 === 1);
console.log('Strict Equality Operator (===). With different type', '1' === 1);

console.log('Lose Equality Operator (==). Only value must be equal', 1 == 1);
console.log('Lose Equality Operator (==). With different type', '1' == 1);
console.log('Lose Equality Operator (==). If types are different, it transforms the rigth side to the same type as the left side and then compares both values', true == 1);
