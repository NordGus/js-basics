// Comparison Operators

let x = 1;

// Relational
console.log('x greater than 0', x > 0);
console.log('x greater than or equal to 1', x >= 1);
console.log('x less than 0', x < 0);
console.log('x less or equal to 1', x <= 1);

// Equality
console.log('x equal to 1', x === 1);
console.log('x different than 1', x !== 1);